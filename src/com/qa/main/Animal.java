package com.qa.main;

//Entity
public class Animal {

	// Fields
	private int eyes = 15;
	private String name;

	public int getEyes() {
		return eyes;
	}

	public String getName() {
		return name;
	}

	public void setEyes(int eyes) {
		this.eyes = eyes;
	}

	public void setName(String name) {
		this.name = name;
	}

}
